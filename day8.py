a = 3 + 4j
b = 1 - 2j

# Addition of the complex numbers
addition = a + b
print("Addition:", addition)

# Subtraction of  the complex numbers
subtraction = a - b
print("Subtraction:", subtraction)

# Multiplication of  the complex numbers
multiplication = a * b
print("Multiplication:", multiplication)

# Division of the complex numbers
division = a / b
print("Division:", division)

# Absolute value (magnitude) of the complex number
magnitude_a = abs(a)
print("Magnitude of a:", magnitude_a)

